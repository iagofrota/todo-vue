<template>
  <div class="new-task">
      <input v-model="name" 
             @keydown.enter="add" 
             type="text"
             placeholder="Nova tarefa"
             class="form-element" />
      <button @click="add" 
              type="button"
              class="form-element btn">+</button>
  </div>
</template>

<script>
export default {
    data() {
        return {
            name: ''
        }
    },
    methods: {
        add() {
            if (this.name.length) {
                this.$emit('taskAdded', { name: this.name }),
                this.name = ''
            }
        }
    }
}
</script>

